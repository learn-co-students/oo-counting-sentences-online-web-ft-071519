require 'pry'

class String

  def sentence?
    self.end_with?(".") ? true : false
  end

  def question?
    self.end_with?("?") ? true : false
  end

  def exclamation?
    self.end_with?("!") ? true : false
  end

  def count_sentences
    split_sent = self.split(/\?|\.|!/)
    split_sent.delete("")
    return split_sent.count
  end
end
